    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // create an instance of entity class
        EmployeeDetails em = new EmployeeDetails();

        if (txtPassword.getText().equals(txtConfirmPassword.getText())) {

            int x = db.checkUsername(txtUsername.getText());

            if (x == 1) {

                em.setRegID(0);
                em.setFirstName(txtFirstName.getText());
                em.setLastName(txtLastName.getText());
                em.setAge(Integer.parseInt(txtAge.getText().toString()));
                em.setCountry(ddCountry.getSelectedItem().toString());
                em.setEmail(txtEmail.getText());
                em.setUsername(txtUsername.getText());
                em.setPassword(txtPassword.getText());

                boolean result = db.addEmployee(em);

                if (result) {

                    //say inserted successfully!!
                    JOptionPane.showMessageDialog(this, "successfully inserted!!");
                    clearFields();
                    this.dispose();

                } else {

                    //insert is not successful
                    JOptionPane.showMessageDialog(this, "Oops Eror occured while inserting!!");

                }

            } else if (x == 0) {

                JOptionPane.showMessageDialog(this, "Username already exists!!");
                txtUsername.setText(null);

            } else {

                JOptionPane.showMessageDialog(this, "Error occured while checking the username!!");
                return;
            }

        } else {

            JOptionPane.showMessageDialog(this, "Password mismatched!!");
            txtPassword.setText("");
            txtConfirmPassword.setText("");
        }

    }//GEN-LAST:event_btnSubmitActionPerformed

